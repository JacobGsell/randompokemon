<script context="module">
	export function getColorByType(type) {
		switch (type) {
			case 'fire':
				return '#ff8e8e';
			case 'normal':
				return '#e0e0e0';
			case 'fighting':
				return '#ffa48e';
			case 'flying':
				return '#8ec2ff';
			case 'poison':
				return '#ce8eff';
			case 'ground':
				return '#ba8773';
			case 'rock':
				return '#929292';
			case 'bug':
				return '#e0e99f';
			case 'ghost':
				return '#d9c6dc';
			case 'steel':
				return '#a8b8cf';
			case 'water':
				return '#8e96ff';
			case 'grass':
				return '#a6f4a5';
			case 'electric':
				return '#fdffa0';
			case 'psychic':
				return '#e58bff';
			case 'ice':
				return '#e074e9';
			case 'dragon':
				return '#fd6b6b';
			case 'dark':
				return '#b9b6dc';
			case 'fairy':
				return '#ffd2f0';
		}
	}
</script>
